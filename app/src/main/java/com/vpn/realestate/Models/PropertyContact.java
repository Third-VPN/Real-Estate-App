package com.vpn.realestate.Models;

public class PropertyContact {
    String property_contact_id, property_contact_details, price, user_id, property_id;

    public String getProperty_contact_id() {
        return property_contact_id;
    }

    public void setProperty_contact_id(String property_contact_id) {
        this.property_contact_id = property_contact_id;
    }

    public String getProperty_contact_details() {
        return property_contact_details;
    }

    public void setProperty_contact_details(String property_contact_details) {
        this.property_contact_details = property_contact_details;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getProperty_id() {
        return property_id;
    }

    public void setProperty_id(String property_id) {
        this.property_id = property_id;
    }
}
